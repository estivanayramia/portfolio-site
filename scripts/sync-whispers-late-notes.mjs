#!/usr/bin/env node
import fs from 'node:fs';

const filePath = 'EN/hobbies/whispers.html';
const startMarker = '<!-- Generated by scripts/sync-whispers-late-notes.mjs -->';
const endMarker = '                        </div>';

const renderSlide = (noteNumber) => [
  '                            <figure class="carousel-slide note-card rounded-xl overflow-hidden bg-yellow-100/50 shadow-sm">',
  `                                <img src="/assets/img/Portolio-Media/Portfolio-Media/whispers-/Note ${noteNumber}.webp" alt="Handwritten sticky note with words of encouragement" class="w-full h-full object-cover" loading="lazy" decoding="async" width="600" height="600">`,
  '                            </figure>'
].join('\n');

const generatedBlock = [
  '                            <!-- Generated by scripts/sync-whispers-late-notes.mjs -->',
  ...Array.from({ length: 9 }, (_, index) => renderSlide(index + 38))
].join('\n');

const source = fs.readFileSync(filePath, 'utf8');
const startIndex = source.indexOf(startMarker);
if (startIndex === -1) {
  throw new Error(`Could not find start marker in ${filePath}`);
}

const endIndex = source.indexOf(endMarker, startIndex);
if (endIndex === -1) {
  throw new Error(`Could not find end marker in ${filePath}`);
}

const nextSource = `${source.slice(0, startIndex)}${generatedBlock}\n${source.slice(endIndex)}`;
if (nextSource !== source) {
  fs.writeFileSync(filePath, nextSource, 'utf8');
}

console.log('[sync-whispers-late-notes] Synced Note 38-46 slide markup to tracked .webp assets');
